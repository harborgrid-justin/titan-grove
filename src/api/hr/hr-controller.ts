  /**
   * Record time entry
   */
  async recordTimeEntry(req: Request, res: Response): Promise<void> {
    try {
      const { employeeId, date, projectId, taskDescription, hours, billable } = req.body;

      // Validate required fields
      if (!employeeId || !date || !projectId || !hours) {
        this.sendError(res, 'Missing required fields: employeeId, date, projectId, hours', 400);
        return;
      }

      // Validate date format (YYYY-MM-DD)
      const dateRegex = /^\d{4}-\d{2}-\d{2}$/;
      if (!dateRegex.test(date)) {
        this.sendError(res, 'Invalid date format. Use YYYY-MM-DD', 400);
        return;
      }

      // Validate hours (must be positive and reasonable)
      if (typeof hours !== 'number' || hours <= 0 || hours > 24) {
        this.sendError(res, 'Invalid hours. Must be between 0 and 24', 400);
        return;
      }

      // Create time entry record
      const newTimeEntry = {
        id: `TIME-${Date.now().toString().slice(-6)}`,
        employeeId,
        date,
        projectId,
        taskDescription: taskDescription || '',
        hours,
        billable: billable !== undefined ? billable : true,
        status: 'SUBMITTED',
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
      };

      // In production, would save to database
      // await db.timeEntries.insert(newTimeEntry);

      this.sendSuccess(res, { timeEntry: newTimeEntry }, 'Time entry recorded successfully', 201);
    } catch (error) {
      this.sendError(res, (error as Error).message || 'Failed to record time entry', 500);
    }
  }
