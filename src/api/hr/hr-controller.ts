  /**
   * Create performance review
   */
  async createPerformanceReview(req: Request, res: Response): Promise<void> {
    try {
      const { employeeId, reviewPeriod, ratings, strengths, areasForImprovement, goals, comments } =
        req.body;

      // Validate required fields
      if (!employeeId || !reviewPeriod || !ratings) {
        this.sendError(res, 'Missing required fields: employeeId, reviewPeriod, ratings', 400);
        return;
      }

      // Validate ratings structure
      const requiredRatingFields = [
        'technicalSkills',
        'communication',
        'teamwork',
        'leadership',
        'problemSolving',
      ];
      for (const field of requiredRatingFields) {
        if (typeof ratings[field] !== 'number' || ratings[field] < 0 || ratings[field] > 5) {
          this.sendError(res, `Invalid rating for ${field}. Must be between 0 and 5`, 400);
          return;
        }
      }

      // Calculate overall rating
      const ratingValues = Object.values(ratings) as number[];
      const overallRating =
        ratingValues.reduce((sum, val) => sum + val, 0) / ratingValues.length;

      // Create review record
      const newReview = {
        id: `REV-${Date.now().toString().slice(-6)}`,
        employeeId,
        reviewPeriod,
        reviewDate: new Date().toISOString().split('T')[0],
        reviewer: 'CURRENT_USER', // Would be from auth context
        ratings,
        overallRating: Math.round(overallRating * 10) / 10,
        strengths: strengths || [],
        areasForImprovement: areasForImprovement || [],
        goals: goals || [],
        status: 'DRAFT',
        comments: comments || '',
        createdAt: new Date().toISOString(),
        updatedAt: new Date().toISOString(),
      };

      this.sendSuccess(res, { review: newReview }, 'Performance review created successfully', 201);
    } catch (error) {
      this.sendError(
        res,
        (error as Error).message || 'Failed to create performance review',
        500
      );
    }
  }

